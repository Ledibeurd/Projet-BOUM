    private void boutCreerModelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boutCreerModelActionPerformed
        String diam = textDiam.getText();
        String nom = textNom.getText();
                
        if (!  Verification.estString(nom))  
        {        
            Messages.warning (this, "Nom modèle incorrect");
            textNom.setText("");
        }
        else  if (! Verification.estNumeric(diam) )
            Messages.warning (this, "Diamètre incorrect");
        else
        {
            float diametre = Float.parseFloat(diam);
            if (ManagerApplication.creerModele(nom, diametre)) 
            {
                Messages.info (this, ManagerApplication.getMessage());
                ManagerApplication.listModel();
                System.out.println("ok");
                ListModel m = (ListModel) listModel.getModel();
                m.refresh();
            } 
            else
            {
                Messages.erreur (this, ManagerApplication.getMessage());                                                 ;
            }

            textNom.setText("");
            textDiam.setText("");
        }
    }//GEN-LAST:event_boutCreerModelActionPerformed
    
    
