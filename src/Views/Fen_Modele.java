    private class EcouteListModel implements ListSelectionListener {

        @Override
        public void valueChanged(ListSelectionEvent e) {
            int index = e.getFirstIndex();
            Modele m = listModel.getSelectedValue();

            // on change la valeur du diametre en String
            // pour pouvoir l'afficher dans le textDiam.
            String diam = String.valueOf(m.getDiametre());

            textDiam.setText(diam);
            textNom.setText(m.getNom());
        }

    }


